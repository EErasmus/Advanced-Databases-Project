CREATE TABLE role (
    rolename            INTEGER
        GENERATED BY DEFAULT AS IDENTITY START WITH 1000,
    role_level          INTEGER,
    privileged          VARCHAR2(1 CHAR),
    role_type           VARCHAR2(1 CHAR) NOT NULL,
    new_role            VARCHAR2(1),
    person_person_id    INTEGER,
    faculty_faculty_id  INTEGER,
    active              VARCHAR2(1),
    eff_date            DATE
);

COMMIT;

