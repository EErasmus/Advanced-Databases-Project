    role_level,
    privileged,
    role_type,
    new_role,
    person_person_id,
    faculty_faculty_id,
    active,
    eff_date
)
    SELECT
        '1',
        NULL,
        'S',
        NULL,
        p.person_id,
        c.faculty_faculty_id,
        'X',
        '01-jan-2020'
    FROM
             person p
        JOIN course c ON ( p.person_id = c.person_person_id );

COMMIT;